//switch case una estructura de control
//que solo evalua igualdad
var key=2;
switch (key) {
    case 1:
        console.log('UNO');
        break;
    case 2:
        console.log('DOS');
        break;
    case 3:
        console.log('TRES');
        break;
    case 4:
        console.log('CUATRO');
        break;
    case 5:
        console.log('CINCO');
        break;
    default:
        console.log('Opción NO válida');
        break;
}